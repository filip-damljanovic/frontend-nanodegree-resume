var map;    // declares a global map variable

/*
Start here! initializeMap() is called when page is loaded.
*/
function initializeMap() {
  var locations;

  var platform = new H.service.Platform({
    'app_id': 'l8wFI8WC2ToCOciTA4Xn',
    'app_code': 'HA94cbPIjbqejgvmWpzn4g'
  });

  // Obtain the default map types from the platform object
  var maptypes = platform.createDefaultLayers();

  // Instantiate (and display) a map object:
  map = new H.Map(document.getElementById('map'), maptypes.normal.map,{
    zoom: 7,
    center: {lat: 52.53075, lng: 13.3851}
  });
  /*
  locationFinder() returns an array of every location string from the JSONs
  written for bio, education, and work.
  */
  function locationFinder() {

    // initializes an empty array
    var locations = [];

    // adds the single location property from bio to the locations array
    locations.push(model.bio.contacts.location);

    // iterates through school locations and appends each location to
    // the locations array. Note that forEach is used for array iteration
    // as described in the Udacity FEND Style Guide:
    // https://udacity.github.io/frontend-nanodegree-styleguide/javascript.html#for-in-loop
    model.education.schools.forEach(function(school){
      locations.push(school.location);
    });

    // iterates through work locations and appends each location to
    // the locations array. Note that forEach is used for array iteration
    // as described in the Udacity FEND Style Guide:
    // https://udacity.github.io/frontend-nanodegree-styleguide/javascript.html#for-in-loop
    model.work.jobs.forEach(function(job){
      locations.push(job.location);
    });

    return locations;
  }

  /*
  pinPoster(locations) takes in the array of locations created by locationFinder()
  and fires off Google place searches for each location
  */
  function pinPoster(locations) {
    var searchResult, error;

    locations.forEach(function(place){
      // Define search parameters:
      var geocodingParams = {
        // Plain text search for places with the word "hotel"
        // associated with them:
        searchText: place
      };
      // Define a callback function to handle data on success:
      function onResult(data) {
        var position, marker;
        searchResult = data.Response.View[0].Result;
        // Add a marker for each location found
        for (i = 0;  i < searchResult.length; i++) {
          position = {
            lat: searchResult[i].Location.DisplayPosition.Latitude,
            lng: searchResult[i].Location.DisplayPosition.Longitude
          };
          marker = new H.map.Marker(position);
          map.addObject(marker);
        }
        map.setCenter({lat: position.lat, lng:position.lng});
      }

      // Get an instance of the geocoding service:
      var geocoder = platform.getGeocodingService();

      // Call the geocode method with the geocoding parameters,
      // the callback and an error callback function (called if a
      // communication error occurs):
      geocoder.geocode(geocodingParams, onResult, function(e) {
        alert(e);
      });
    });
  }

  // locations is an array of location strings returned from locationFinder()
  locations = locationFinder();

  // pinPoster(locations) creates pins on the map for each location in
  // the locations array
  pinPoster(locations);

}

